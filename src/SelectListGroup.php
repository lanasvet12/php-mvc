<?php
namespace PhpMvc;

/**
 * Represents the optgroup HTML element and its attributes.
 */
class SelectListGroup {

    /**
     * Gets or sets a value that indicates whether this SelectListGroup is disabled.
     * 
     * @var bool
     */
    public $disabled;

    /**
     * Represents the value of the optgroup's label.
     * 
     * @var string
     */
    public $name;

}